package com.example.model;

public class Employee {

	// JAVA POJO or Bean
	// 3 attribute of properties
	private int id;
	private String name;
	private String role;
	
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getRole() {
		return role;
	}
	
	public void setRole(String role) {
		this.role = role;
	}
	
	
	
	
}
